        <br><h4>Se requiere realizar una aplicación cuyo modelo de negocio consiste en que personas puedan brindar el servicio del cuidado de mascotas. Dicho cuidado se trata de una estadía corta a cambio de una remuneración.</h4>
        <h4>Los usuarios que se registren como Guardianes, tienen un perfil en el sitio donde exponen que tipo de perro están dispuestos a cuidar (pequeño, mediano o grande) y la remuneración esperada por la estadía.</h4>
        <h4>Por otro lado, existe el tipo de usuario Dueño que registra un nuevo perfil en la aplicación y será quien contrate el servicio de los Guardianes. Una vez completado el alojamiento del perro, los Dueños tienen la habilidad de generar una review sobre el servicio. Estas reviews generarán en el Guardián una mayor reputación dentro de la aplicación</h4>
        <h4>La aplicación les permite a los usuarios Guardianes, definir los días específicos que cuentan con disponibilidad para el cuidado de perros. Esta información será de utilidad para los Dueños al momento de reservar el servicio.</h4>
        <h4>Con motivo de seguridad para las mascotas, un Guardián solamente puede cuidar a un perro por estadía.</h3>
        <h4>Los Dueños deberán crearle un perfil a cada mascota que poseen. Por cada perfil de mascota, deben cargar: una foto, raza, tamaño, plan de vacunación (como imagen) y observaciones generales de la misma. La aplicación también brinda la oportunidad de subir un video del perro.</h4>
        <h4>Cuando un Dueño selecciona un Guardián de su agrado, se generará una reserva en el sistema entre las fechas que requiere. El Guardián en cuestión, deberá aceptar o rechazar esta nueva reserva. </h4>
        <h4>En caso de que la reserva sea aceptada por el Guardián, se envía un cupón de pago al Dueño con el 50% del costo del total de la estadía. Al momento de efectuar el pago, la reserva queda confirmada</h4>
        
        <br><h2><strong>Requisitos no funcionales:</strong></h2>
        <h4>Programación en capas de la aplicación respetando la arquitectura de 3 capas lógicas vista durante la cursada. Esto implica el desarrollo de las clases que representen las entidades del modelo y controladoras de los casos de uso, las vistas y la capa de acceso a datos.</h4>
        
